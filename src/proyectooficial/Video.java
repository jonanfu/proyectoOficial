    /**
     * Creates new form Videos
     */
    public  JFXPanel jfxPanel = new JFXPanel(); 
    private File file;
    private MediaPlayer oracleVid;
    private String video;
    public Video(String video)
    {
        this.video = video;
        initComponents();
        createScene();      
        setLayout(new BorderLayout());
        add(jfxPanel,BorderLayout.CENTER);
        
    }
        private void createScene(){
        Platform.runLater(new Runnable() {
             @Override
             public void run() {                 
                file = new File(video);                                  
                    oracleVid = new MediaPlayer(                                       
                        new Media(file.toURI().toString())
                    );
                    //se a√±ade video al jfxPanel
                    jfxPanel.setScene(new Scene(new Group(new MediaView(oracleVid))));                    
                    oracleVid.setCycleCount(MediaPlayer.INDEFINITE);//repite video
                    
             }
        });
    }
    
    public void play(){
        oracleVid.play();
    }
    public void pausa(){
        oracleVid.pause();
    }
    public void silencio(){
        oracleVid.setMute(true);
    }
    public void sonido (){
        oracleVid.setMute(false);
    }
    public void volumen(int volumen){
        
        oracleVid.setVolume(volumen);
    }

    public void setVideo(String video) {
        this.video = video;
    }
    

